// While loop
let num = 0; //loop variable
while (num < 5) { //loop condition
    console.log(num); //loop statement
    num++; //loop variable change
}

//------ For Loop structure-------

/**
 * for (first part ; second part ; third part){
 * 
 *  loop statement
 * }
 * 
 */

for (let num = 0; num < 5; num++){
    console.log(num);
}